package Aula_1406;

public class Foreach {

	public static void main(String[] args) {
		
		int[] idade = {10, 16, 27};
		
		System.out.println("varrendo o foreach....");
		
		for(int obj_indice : idade) {
			System.out.println(obj_indice);
			
		}//fim for

	}//fim main

}//fim Foreach

